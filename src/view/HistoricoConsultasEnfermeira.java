        ArmazenaConsulta.getConsultasporCoren(coren).forEach(consulta -> this.taConsultasEnfermeira.append("\n"+consulta.getPaciente().getNome()+" | "+consulta.getMedico().getNome()+" | "+consulta.getEnfermeira().getNome()+" | "+consulta.getPaciente().getCartaoDeAgendamento()+" | "+consulta.getData().getDia()+"/"+consulta.getData().getMes()+"/"+consulta.getData().getAno()+"\n"));
